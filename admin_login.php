<?php
session_start();
include "db/db_connection.php";  // Adjust the path as necessary

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $username = $_POST["username"];
    $password = $_POST["password"];

    // Prepare and execute the query for admin authentication
    $stmt = $conn->prepare("SELECT * FROM admins WHERE username = ?");
    $stmt->bind_param("s", $username);
    $stmt->execute();
    $result = $stmt->get_result();
    $admin = $result->fetch_assoc();

    if ($admin && password_verify($password, $admin['password'])) {
        // Set session variables and redirect to admin dashboard
        $_SESSION['admin_id'] = $admin['id'];
        $_SESSION['is_admin'] = true;
        header("Location: admin_dashboard.php");
        exit();
    } else {
        echo "<p class='alert alert-danger'>Invalid username or password.</p>";
    }
}
